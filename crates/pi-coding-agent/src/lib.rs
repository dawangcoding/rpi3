//! AI 编程助手 CLI 入口
#![warn(missing_docs)]
//!
//! pi-coding-agent 是 AI 编程助手的命令行界面，整合了所有核心模块：
//!
//! - **CLI 模块**: 命令行参数解析和入口点
//! - **配置模块**: 应用配置和用户偏好
//! - **核心模块**: 会话管理、工具系统、权限控制、扩展系统
//! - **交互模式**: TUI 交互、消息渲染、主题系统
//!
//! # 内置工具
//!
//! - `read`: 读取文件内容
//! - `write`: 写入文件
//! - `edit`: 编辑文件（搜索替换）
//! - `bash`: 执行 Shell 命令
//! - `grep`: 文本搜索
//! - `find`: 文件搜索
//! - `ls`: 目录列表
//!
//! # 扩展系统
//!
//! 支持通过扩展注册自定义工具和 Slash 命令。

/// CLI 模块
pub mod cli;
/// 配置模块
pub mod config;
/// 核心模块
pub mod core;
/// 交互模式模块
pub mod modes;
/// RPC 模块
